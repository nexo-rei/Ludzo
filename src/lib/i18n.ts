let currentLanguage: Language = 'en';

export function setLanguage(lang: Language): void {
  currentLanguage = lang;
}

export function getCurrentLanguage(): Language {
  return currentLanguage;
}

export function t(key: string, lang?: Language): string {
  const lng = lang ?? currentLanguage;
  return translations[lng]?.[key] ?? translations['en'][key] ?? key;
}

export function detectLanguage(telegramLang?: string): Language {
  if (!telegramLang) return 'en';
  const code = telegramLang.split('-')[0].toLowerCase();
  const map: Record<string, Language> = {
    en: 'en', ru: 'ru', uk: 'uk', es: 'es', pt: 'pt',
    fr: 'fr', de: 'de', it: 'it', tr: 'tr', hi: 'hi',
  };
  return map[code] ?? 'en';
}
